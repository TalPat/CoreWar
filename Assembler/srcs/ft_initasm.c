#include "asm.h"

void	ft_initasm(t_asm *asmb)
{
	int	i;

	i = 0;
	while (i < 16)
	{
		asmb->error[0] = 0;
		i++;
	}
	asmb->errorcnt = 0;
	ft_inittable(asmb);
}
